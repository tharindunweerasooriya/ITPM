    public void tableload() {
    
        try {
            String sql = "SELECT id AS ID,tagName AS 'TAG NAME',tagCode AS 'TAG CODE',relatedTag AS 'RELATED TAG' FROM tag";
            rs = st.executeQuery(sql);
            tagTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    public void tabledata() {
    
        int r = tagTable.getSelectedRow();
        
        String tagName = tagTable.getValueAt(r, 1).toString();
        String tagCode = tagTable.getValueAt(r,2).toString();
        String relatedTag = tagTable.getValueAt(r, 1).toString();
        
        tagNameBox.setText(tagName);
        tagCodeBox.setText(tagCode);
        relatedTagBox.setSelectedItem(relatedTag);
        
    }
    
    public void clear() {
        
        tagNameBox.setText("");
        tagCodeBox.setText("");
        relatedTagBox.setSelectedIndex(0);
    
    }
    
    public void update() {
    
        int r = tagTable.getSelectedRow();
        int value = (int) (tagTable.getModel().getValueAt(r, 0));
        
        String tagName = tagNameBox.getText();
        int tagCode = Integer.parseInt(tagCodeBox.getText());
        String relatedTag = relatedTagBox.getSelectedItem().toString();
        
        try {
            String sql = "UPDATE tag SET tagName='"+tagName+"',tagCode='"+tagCode+"',relatedTag='"+relatedTag+"' "
                    + "WHERE id='"+value+"'";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Tag updated successfully");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
        tableload();
    }

