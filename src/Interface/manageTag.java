    private void tagNameBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tagNameBoxKeyReleased
        // TODO add your handling code here:
        String PATTERN ="^[a-zA-Z]{1,100}$";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(tagNameBox.getText());
        if(!match.matches()){
            nameV.setText("Incorrect input");
        }else{
            nameV.setText(null);
        }
    }//GEN-LAST:event_tagNameBoxKeyReleased

