    private void lecturerComboBox(){
    
        try{
        
            pst = con.prepareStatement("select id from lecturers");
            
            rs = pst.executeQuery();
            
            while(rs.next()){
                String ID = Integer.toString(rs.getInt("id"));
                lecturer.addItem(ID);
            }
            
            
            
        
        } catch (SQLException ex) {
            Logger.getLogger(NotAvailableTime.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
                    
                        try{
                        
                            rs.close();
                            pst.close();
                            
                        }catch(Exception e){}
                        
                    }
        }
    
    
    
