    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:

        String lecturer = this.lecturer.getSelectedItem().toString();
        String group = this.group.getSelectedItem().toString();
        String subgroup = this.subGroup.getSelectedItem().toString();
        String session = this.session.getSelectedItem().toString();
        String start = this.start.getSelectedItem().toString();
        String end = this.end.getSelectedItem().toString();
        String day = this.day.getSelectedItem().toString();

        try{

            pst = con.prepareStatement("insert into notAvailableTimes (startTime,endTime,lecturer,groupID,subGroup,sessionID,day) values (?,?,?,?,?,?,?)");

            pst.setString(1, start);
            pst.setString(2, end);
            pst.setString(3, lecturer);
            pst.setString(4, group);
            pst.setString(5, subgroup);
            pst.setString(6, session);
            pst.setString(7, day);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Not Available Time Added");

        } catch (SQLException ex) {
            Logger.getLogger(NotAvailableTime.class.getName()).log(Level.SEVERE, null, ex);
        }finally{

            try{

                pst.close();

            }catch(Exception e){}

        }

    }//GEN-LAST:event_submitActionPerformed

